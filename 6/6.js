//Simple Multiplication

// This kata is about multiplying a given number by eight if it is an even number and by nine otherwise.


//Pseudocode: 
//1. If a number is even, multiply it by 8.
    //else multiply by 8.

//2. return value
function simpleMultiplication(number){

  if (number%2===0){
     return number * 8;
    } else{
      return number * 9;
    }
}

